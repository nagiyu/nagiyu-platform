/**
 * 自殺・自傷・希死念慮を示すキーワード辞書（Phase 2d / Issue #3250）。
 *
 * 方針:
 * - false negative ゼロを目指す（false positive は許容、安全側に倒す）
 * - カテゴリ別に分類し、活用形・表記揺れに対応した正規表現を使用
 * - 慣用句・比喩表現（「死ぬほど美味しい」等）は除外リストでハンドリング
 *
 * @see tasks/livetalk/external-design.md ADR-009
 * @see Issue #3250
 */

import type { SafetyCategory } from './types.js';

/** カテゴリ別のキーワードパターン */
export const KEYWORD_PATTERNS: Record<SafetyCategory, RegExp[]> = {
  // 自殺念慮：死にたい、消えたい、いなくなりたい 等
  suicidal_ideation: [
    /死に?たい/,
    /消え(?:て)?(?:しまい)?たい/,
    /いなくなり?たい/,
    /消えてしまいたい/,
    /いなく(?:なって)?しまいたい/,
    /自殺(?:したい|を考|しよう|するつもり|について|に関して)?/,
    /死ぬ(?:つもり|ことを考|ことについて)/,
    /死を(?:考え|選|望|求)/,
    /もう(?:死に?たい|終わりにしたい|消えたい)/,
    /命を(?:絶ち?たい|絶とう|断ち?たい)/,
  ],

  // 自傷行為：リスカ、自傷、手首を切る 等
  self_harm: [
    /自傷/,
    /リスカ/,
    /リストカット/,
    /手首を?切/,
    /腕を?切/,
    /身体を?傷つけ/,
    /自分を?傷つけ/,
    /痛め(?:つけ)?たい/,
    /切り(?:つけ|刻み?)たい/,
  ],

  // 希死念慮・絶望：生きていたくない、生きる意味がない 等
  hopelessness: [
    /生きていたくない/,
    /生きる意味が?ない/,
    /生きていても(?:仕方|意味)が?ない/,
    /生きていたくない/,
    /もう生きていたくない/,
    /存在し(?:ていたくない|てはいけない)/,
    /生きるのが(?:つらい|苦しい|嫌だ|辛い)/,
    /もう(?:生きて)?い(?:ら)?れない/,
    /死んでしまいたい/,
    /死んだ(?:ほうが|方が)(?:いい|まし)/,
  ],

  // 自殺手段への言及：首を吊る、飛び降り、薬の大量摂取 等
  crisis_method: [
    /首を?(?:吊|縊)/,
    /飛び(?:降り|下り)/,
    /飛び込み(?:たい|自殺)?/,
    /OD(?:\s*する|\s*したい)/i,
    /薬を?大量(?:に)?(?:飲|服用|摂取)/,
    /大量(?:服薬|摂取|飲酒)/,
    /睡眠薬.*?大量/,
    /刃物で?(?:自分|身体|手首|腕)/,
    /高いところから?(?:飛び|落ち)/,
  ],

  // 危機的精神状態：もう終わりにしたい、助けてほしい（文脈依存） 等
  crisis_state: [
    /もう(?:全部|すべて|何もかも)終わりにしたい/,
    /もう限界で?(?:す|だ)?(?:。|$|\s)/,
    /何も(?:かも)?嫌になった/,
    /生きているのが(?:辛い|つらい|苦しい)(?:。|$|\s)/,
    /誰にも?(?:理解されない|わかってもらえない)(?:。|$|\s)/,
  ],
};

/**
 * 除外パターン（慣用句・比喩表現）。
 * これらのパターンにマッチした入力は、上記キーワードが含まれていても介入しない。
 *
 * ただし除外を適用するのはパターン単体で文が完結している場合のみ。
 * 「死にたいし、死ぬほど疲れた」のような複合表現は検出側でキャッチされる。
 */
export const EXCLUSION_PATTERNS: RegExp[] = [
  // 「死ぬほど〜」= 程度の強調（「死ぬほど疲れた」「死ぬほど美味しい」等）
  /死ぬほど[^\s、。！？!?\n]*(?:[^\s、。！？!?\n]|$)/,
  // 「死に物狂い」= 慣用句
  /死に物狂い/,
  // 「消えたいほど恥ずかしい」= 程度の強調（比較的軽微）
  // ↑ これは意図的に除外しない（恥ずかしさの文脈でも心理的苦痛の可能性があるため）
];

/**
 * 入力テキストが除外パターンのみで構成されているかを確認する。
 * 除外パターンを適用してもキーワードが残る場合は検出対象とみなす。
 */
export function shouldExclude(input: string, matchedPattern: RegExp): boolean {
  // マッチした部分が除外パターンの一部として含まれている場合は除外
  for (const exclusion of EXCLUSION_PATTERNS) {
    if (exclusion.test(input)) {
      // 除外パターンで入力全体が説明できる（キーワードが除外文脈内にある）場合のみ除外
      const withoutExcluded = input.replace(exclusion, '');
      if (!matchedPattern.test(withoutExcluded)) {
        return true;
      }
    }
  }
  return false;
}
